package GameZone;

public class CardInfo
{
    private int num;
    private String form;

    public void setNum(int numFinal)
    {
        num = numFinal;
    }
    public int getNum()
    {
        return num;
    }
    public void setForm(String formFinal)
    {
        form = formFinal;
    }
    public String getForm()
    {
        return form;
    }
}
